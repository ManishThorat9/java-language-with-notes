public class MathFunction {
  public static void main(String[] args){
    
    System.out.println(Math.abs(-4.7)); // 4.7
    System.out.println(Math.sqrt(16)); // 4.0
    System.out.println(Math.pow(2,3)); // 8.0
    System.out.println(Math.log(Math.E)*Math.log(5)); // 1.60943
    System.out.println(Math.round(12.345)); // 12
    System.out.println(Math.ceil(12.345)); // 13
    System.out.println(Math.floor(12.345)); // 12
    System.out.println(Math.max(1, 02));   //2
    System.out.println(Math.min(1, 02));  //1
    System.out.println(Math.random()); // random number between 0 and 1
    System.out.println(Math.incrementExact(5)); // increment by 1
    int number =(int)(Math.random()*101); 
    System.out.println(number); // random number between 0 and 100
  }

}
